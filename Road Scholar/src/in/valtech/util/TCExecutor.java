package in.valtech.util;

public class TCExecutor
{
	private String execute;
	private String module;
	private String tc_Description;
	private String priority;
	private String tc_Name;
	private String reusable_TCID;
	private String ds_Config;
	private String driver;
	private String homepage_url;
	private String UserName;
	private String Password;
	
	
	public String getExecute() {
		return execute;
	}
	public void setExecute(String execute) {
		this.execute = execute;
	}
	public String getModule() {
		return module;
	}
	public void setModule(String module) {
		this.module = module;
	}
	public String getTc_Description() {
		return tc_Description;
	}
	public void setTc_Description(String tc_Description) {
		this.tc_Description = tc_Description;
	}
	public String getPriority() {
		return priority;
	}
	public void setPriority(String priority) {
		this.priority = priority;
	}
	public String getTc_Name() {
		return tc_Name;
	}
	public void setTc_Name(String tc_Name) {
		this.tc_Name = tc_Name;
	}
	public String getReusable_TCID() {
		return reusable_TCID;
	}
	public void setReusable_TCID(String reusable_TCID) {
		this.reusable_TCID = reusable_TCID;
	}
	public String getDs_Config() {
		return ds_Config;
	}
	public void setDs_Config(String ds_Config) {
		this.ds_Config = ds_Config;
	}
	public String getDriver() {
		return driver;
	}
	public void setDriver(String driver) {
		this.driver = driver;
	}
	public String getHomepage_url() {
		return homepage_url;
	}
	public void setHomepage_url(String homepage_url) {
		this.homepage_url = homepage_url;
	}
	public String getUserName() {
		return UserName;
	}
	public void setUserName(String userName) {
		UserName = userName;
	}
	public String getPassword() {
		return Password;
	}
	public void setPassword(String password) {
		Password = password;
	}
	
	
}
	
	